    private void confirmbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmbtnActionPerformed
        String name = mr_name.getText();
        String tpNumber = mr_tp.getText();
        String password = mr_password.getText();
        String rePassword = mr_reEnterPW.getText();  
        String contact = mr_contact.getText();
        String eContact = mr_eContact.getText();
        String email = mr_email.getText();
        String dateBirth = mr_birth.getText();
        String address = mr_address.getText();
        String country = mr_country.getText();
        Object roomType = mr_roomType.getSelectedItem();
        String referenceNum = mr_referenceNum.getText();

        if (!password.equals(rePassword)) {
            JOptionPane.showMessageDialog(null, "Passwords do not match. Please try again.");
            return;
        }

        if (password.length() < 8) {
            JOptionPane.showMessageDialog(null, "Password must be at least 8 characters long.");
            return;
        }

        if (isTpNumberRegistered(tpNumber)) {
            JOptionPane.showMessageDialog(null, "This TP Number is already registered.");
            return;
        }

        try {
            FileWriter infoWriter = new FileWriter("txtUserInfo.txt", true);

            infoWriter.write("residentName:" + name + "\n" + 
                             "tpNum:" + tpNumber + "\n" + 
                             "password:" + password + "\n" + 
                             "rePassword:" + rePassword + "\n" +
                             "contact:" + contact + "\n" + 
                             "eContact:" + eContact + "\n" + 
                             "email:" + email + "\n" + 
                             "birth:" + dateBirth + "\n" + 
                             "address:" + address + "\n" + 
                             "country:" + country + "\n" + 
                             "roomType:" + roomType + "\n" + 
                             "roomAssigned: " + "\n" + 
                             "referenceNum:" + referenceNum + "\n" + 
                             "---------------------------------------------------------------\n");

            infoWriter.close();

            JOptionPane.showMessageDialog(this, "Sign-up Successful!");


        } catch(IOException e) {
            System.out.println("Error occurred while saving user details...");
            e.printStackTrace();
        }
    
    }//GEN-LAST:event_confirmbtnActionPerformed

    private boolean isTpNumberRegistered(String tpNumber) {
        File file = new File("txtUserInfo.txt");

        if (!file.exists()) {
            return false; 
        }

        try (BufferedReader reader = new BufferedReader(new FileReader(file))) {
            String line;
            while ((line = reader.readLine()) != null) {
                if (line.startsWith("tpNum:")) {
                    String existingTpNumber = line.split(":", 2)[1].trim();
                    if (existingTpNumber.equals(tpNumber)) {
                        return true; 
                    }
                }
            }
        } catch (IOException e) {
            e.printStackTrace();
        }

        return false; // TP Number not found in the file
    }
    
